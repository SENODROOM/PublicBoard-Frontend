const IssueCard = ({ issue }) => {
  const formatDate = (dateString) => {
    const date = new Date(dateString);
    const now = new Date();
    const diffTime = Math.abs(now - date);
    const diffDays = Math.floor(diffTime / (1000 * 60 * 60 * 24));
    
    if (diffDays === 0) return 'Today';
    if (diffDays === 1) return 'Yesterday';
    if (diffDays < 7) return `${diffDays} days ago`;
    
    return date.toLocaleDateString('en-US', {
      year: 'numeric',
      month: 'short',
      day: 'numeric'
    });
  };

  const truncateText = (text, maxLength) => {
    if (!text) return '';
    return text.length > maxLength ? text.substring(0, maxLength) + '...' : text;
  };

  return (
    <div className="group issue-card">
      {/* Top Color Bar based on status */}
      <div className={`h-1 w-full ${
        issue.status === 'Open' ? 'bg-gradient-to-r from-red-400 to-red-600' :
        issue.status === 'In Progress' ? 'bg-gradient-to-r from-yellow-400 to-orange-500' :
        issue.status === 'Pending Review' ? 'bg-gradient-to-r from-blue-400 to-blue-600' :
        'bg-gradient-to-r from-emerald-400 to-emerald-600'
      }`} />
      
      <div className="p-6">
        {/* Header */}
        <div className="flex justify-between items-start mb-4">
          <StatusBadge status={issue.status} size="sm" />
          <span className="text-xs font-medium text-gray-400 bg-gray-50 px-2.5 py-1 rounded-full">
            {formatDate(issue.createdAt)}
          </span>
        </div>
        
        {/* Title */}
        <h3 className="text-lg font-bold text-gray-900 mb-3 line-clamp-2 group-hover:text-emerald-600 transition-colors">
          {issue.title}
        </h3>
        
        {/* Description */}
        <p className="text-gray-600 text-sm mb-4 line-clamp-3 leading-relaxed">
          {truncateText(issue.description, 120)}
        </p>
        
        {/* Category & Location */}
        <div className="flex flex-wrap gap-2 mb-4">
          <span className="inline-flex items-center gap-1.5 px-3 py-1.5 rounded-full text-xs font-medium bg-emerald-50 text-emerald-700 border border-emerald-100">
            {categoryIcons[issue.category] || categoryIcons['Other']}
            {issue.category}
          </span>
          {issue.location && (
            <span className="inline-flex items-center px-3 py-1.5 rounded-full text-xs font-medium bg-gray-100 text-gray-600">
              <svg className="w-3.5 h-3.5 mr-1" fill="none" viewBox="0 0 24 24" stroke="currentColor">
                <path strokeLinecap="round" strokeLinejoin="round" strokeWidth={2} d="M17.657 16.657L13.414 20.9a1.998 1.998 0 01-2.827 0l-4.244-4.243a8 8 0 1111.314 0z" />
                <path strokeLinecap="round" strokeLinejoin="round" strokeWidth={2} d="M15 11a3 3 0 11-6 0 3 3 0 016 0z" />
              </svg>
              {truncateText(issue.location, 15)}
            </span>
          )}
        </div>
        
        {/* Footer */}
        <div className="pt-4 border-t border-gray-100 flex items-center justify-between">
          <div className="flex items-center space-x-2">
            <div className="w-8 h-8 rounded-full bg-gradient-to-br from-emerald-400 to-emerald-600 flex items-center justify-center text-white text-sm font-bold shadow-sm">
              {issue.reporterName.charAt(0).toUpperCase()}
            </div>
            <span className="text-sm text-gray-600">
              <span className="font-medium text-gray-900">{issue.reporterName}</span>
            </span>
          </div>
          <Link
            to={`/issues/${issue._id}`}
            className="inline-flex items-center text-sm font-semibold text-emerald-600 hover:text-emerald-700 transition-colors group/link"
          >
            View Details
            <svg className="w-4 h-4 ml-1 transform group-hover/link:translate-x-1 transition-transform" fill="none" viewBox="0 0 24 24" stroke="currentColor">
              <path strokeLinecap="round" strokeLinejoin="round" strokeWidth={2} d="M9 5l7 7-7 7" />
            </svg>
          </Link>
        </div>
      </div>
    </div>
  );
};

export default IssueCard;
